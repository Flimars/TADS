<?php
/* No PHP, a função count() é usada para contar todos os elementos em um array, 
   ou algo de um objeto. Aqui está um exemplo didático:
*/   
    $frutas = array("Maçã", "Banana", "Manga", "Laranja", "Abacaxi");
    echo "O array de frutas contém " . count($frutas) . " elementos.";

/*
Neste exemplo, criamos um array chamado $frutas que contém cinco elementos. 
Quando usamos a função count() no array $frutas, ela retorna o número de elementos 
no array, que neste caso é 5. Portanto, a saída será:  

        O array de frutas contém 5 elementos.

*/

